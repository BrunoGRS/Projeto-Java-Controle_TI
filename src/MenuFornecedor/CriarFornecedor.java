    private void VoltarEstoque1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VoltarEstoque1ActionPerformed
        
        String CNPJFornecedor = jTextField1.getText();
        String NomeFornecedor = jTextField2.getText();
        String EmailFornecedor = jTextField3.getText();
        String TelefoneFornecedor = jTextField4.getText();
        String NumEndFornecedor = jTextField6.getText();
        String CidadeFornecedor = jTextField7.getText();
        String EstadoFornecedor = jTextField8.getText();
        String RuaFornecedor = jTextField5.getText();
        
        int num = Integer.parseInt(NumEndFornecedor);
        
        try{
            Connection connect = getConexao();
            statement  = connect.createStatement();
            
            String query = "INSERT INTO app_java.fornecedor (CNPJFornecedor, NomeFornecedor, EmailFornecedor, TelefoneFornecedor"
                    + ", RuaFornecedor, NumFornecedor, CidadeFornecedor, EstadoFornecedor) VALUES"
                    + " ('" + CNPJFornecedor + "','"+ NomeFornecedor +"', '"+ EmailFornecedor +"', '"+ TelefoneFornecedor +"'"
                    + ", '"+ RuaFornecedor +"', "+ NumEndFornecedor +", '"+ CidadeFornecedor +"', '"+ EstadoFornecedor +"' );";
            
            statement.executeUpdate(query);
            
            
            JOptionPane.showMessageDialog(null, "Fornecedor cadastrado com sucesso!");
        }catch (SQLException ex){
            Logger.getLogger(ConexaoBD.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar Fornecedor");
        }
    }//GEN-LAST:event_VoltarEstoque1ActionPerformed

