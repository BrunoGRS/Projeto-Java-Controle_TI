    private void VoltarEstoque1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VoltarEstoque1ActionPerformed

        String id = jTextField5.getText();
        int i = Integer.parseInt(id);
        String Nome = jTextField2.getText();
        String Email = jTextField3.getText();
        String Login = jTextField4.getText();
        String Senha = jPasswordField1.getText();

        try{
            Connection connect = getConexao();
            statement  = connect.createStatement();

            String query = "UPDATE app_java.usuario SET NomeUsuario = '" + Nome + "',"
                    + "EmailUsuario = '"+ Email +"',LoginUsuario = '"+ Login +"', SenhaUsuario = '"+ Senha +"' WHERE"
                    + " IdUsuario = "+ id +";";

            statement.executeUpdate(query);

            JOptionPane.showMessageDialog(null, "Usuário Atualizado com sucesso!");
        }catch (SQLException ex){
            Logger.getLogger(ConexaoBD.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao Atualizar Usuário");
        }
    }//GEN-LAST:event_VoltarEstoque1ActionPerformed

