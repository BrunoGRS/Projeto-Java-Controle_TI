    private void VoltarEstoque2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VoltarEstoque2ActionPerformed
        
        try{
            Connection connect = getConexao();
            statement  = connect.createStatement();
            
            String id = jTextField5.getText();
            int i = Integer.parseInt(id);
            
            String query = "SELECT * FROM app_java.usuario WHERE IdUsuario =" + i + ";";
            
            ResultSet rs = statement.executeQuery(query);
            
            while(rs.next()){
                String nome = rs.getString("NomeUsuario");
                String email = rs.getString("EmailUsuario");
                String login = rs.getString("LoginUsuario");
                String senha = rs.getString("SenhaUsuario");
                
                jTextField2.setText(nome);
                jTextField3.setText(email);
                jTextField4.setText(login);
                jPasswordField1.setText(senha);
            }
            
            
        }catch (SQLException ex){
            Logger.getLogger(ConexaoBD.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_VoltarEstoque2ActionPerformed

