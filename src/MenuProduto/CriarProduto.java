    private void VoltarEstoque3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VoltarEstoque3ActionPerformed


        String nome = jTextField1.getText();
        String marca = jTextField2.getText();
        String quantidade = jTextField4.getText();
        String CNPJ = jTextField5.getText();
        String user = jTextField6.getText();
        
        int id = Integer.parseInt(user);
        int num = Integer.parseInt(quantidade);

        try{
            Connection connect = getConexao();
            statement  = connect.createStatement();

            String query = "INSERT INTO app_java.produto (NomeProduto, MarcaProduto, QuantidadeProduto"
            + ", F_CNPJ, U_ID) VALUES"
            + " ('" + nome + "','"+ marca +"', "+ num +""
            + ", '"+ CNPJ +"', "+ id +");";

            statement.executeUpdate(query);

            JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!");
        }catch (SQLException ex){
            Logger.getLogger(ConexaoBD.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar Produto");
        }
    }//GEN-LAST:event_VoltarEstoque3ActionPerformed

